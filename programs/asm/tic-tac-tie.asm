GAME_SCREEN_X_OR_O:
	LDA KEY
	CMP #KEY_X
	BEQ PLAYER_WANTS_X
	CMP #KEY_O
	BEQ GAME_SCREEN_DONE
	JMP GAME_SCREEN_X_OR_O
PLAYER_WANTS_X:
