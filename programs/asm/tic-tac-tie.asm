	BEQ PLAYER_ON_6_START
	CMP #KEY_7
	BEQ PLAYER_ON_7_START
	CMP #KEY_8
	BEQ PLAYER_ON_8_START
	CMP #KEY_9
	BEQ PLAYER_ON_9_START
	JMP GAME_SCREEN

