; ==========================================================================================================================================================
;
; POINTERS:
;	$00-$05		Used for drawing stuff
;	$10-$11		The player's character (X or O)
;	$12-$13		The CPU's character
; ==========================================================================================================================================================

	ORG $05CC
	PROCESSOR 6502
	include "geekrig4000/memory-map.asm"

MAIN:
	JSR CLEAR_BOARD
	JSR CLEAR_ERROR
	JSR TITLE_SCREEN
	JSR CLEAR_TITLE
	JSR DRAW_BOARD
	JSR GAME_SCREEN
	JMP MAIN

CLEAR_BOARD:
	LDA #<BOARD_LINE_1
	STA $00
	LDA #>BOARD_LINE_1
	STA $01
	LDA #<BOARD_LINE_2
	STA $02
	LDA #>BOARD_LINE_2
	STA $03
